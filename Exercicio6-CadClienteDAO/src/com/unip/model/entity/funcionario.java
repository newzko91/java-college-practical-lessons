package com.unip.model.entity;

public class funcionario {
	
	private String Nome;
	private String Area;
	private int ID;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getArea() {
		return Area;
	}
	
	public void setArea(String area) {
		Area = area;
	}
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	
}
